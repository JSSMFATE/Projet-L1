 <h3 id="hab">Mission habité vers Mars (NASA)</h3><!faire page pour ce projet>
 
 <p id="habimars">
 Le but de cette mission est denvoyer l'homme sur mars et de la faire revenir sur terre comme 
 la mission sur la Lune.Cette mission permettrais d'en apprendre plus sur Mars mais de voir aussi 
 comment les Hommes réagissent à un sejour sur la planète rouge.Cette mission est probablement 
 la premiere étape avant une quelquonques colonisation de la planète.
 Les missions habitées vers Mars ont fait l'objet d'un très grand nombre d'études. Un document de
  la NASA de 2001 recensait ainsi près de 1000 projets plus ou moins détaillés, 
 produits depuis 1950 au sein de l'agence spatiale américaine ou dans d'autres institutions.
 Le programme est piloté principalement par la NASA .<br>
